    public void actionPerformed(ActionEvent e)//e es el evento que se ejecuta al hacer clic
    {
        Cuadro temp2 = (Cuadro)e.getSource();//nos va a decir que objeto le dimos clic
        if(temp2.estaMinado())
        {
            javax.swing.JOptionPane.showMessageDialog(this, "Oops! Explotaste una mina, perdiste :c");
            this.contador=0;//para que comience de nuevo
        }
        else//Si no esta minado el juego sigue
        {
            temp2.setBackground(Color.CYAN);
            contador++;
            if((numeroCuadros-contadorMinas)==contador)
                javax.swing.JOptionPane.showMessageDialog(this,"GANASTE!");
        }
    }

