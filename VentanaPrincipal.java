    //METODO DE INICIO DEL JUEGO
    
    private void iniciar()
    {
        this.PanelJuego.removeAll();//todos los objetos que esten guardados los va a destruir y va a comezar un nuevo tablero
        int nFilas, nCol;
        nFilas = Integer.parseInt(this.Filas.getText());//voy a obtener el texto del campo filas y lo voy a convertir al numero de filas
        nCol = Integer.parseInt(this.Columnas.getText());//hacemos lo mismo con las columnas
        
        this.PanelJuego.setLayout(new java.awt.GridLayout(nFilas,nCol));
        
        this.numeroCuadros=nFilas*nCol;
        this.contadorMinas=0;//para que cada vez que incie el juego este en 0
        
        for(int i=0;i<nFilas;i++)
        {
            for(int j=0;j<nCol;j++)//Cada vez que ejecute el ciclo voy a crear un nuevo boton
            {
                Cuadro temp = new Cuadro();//mi cuadro se crea y esta minado
                temp.addActionListener(this);//todos los botones van a ejecutar el metodo de arriba
                if(temp.estaMinado())
                    this.contadorMinas++;//entonces mi contador se va a agregar en 1
                temp.setVisible(true);//coloco mi cuadro temporal visible
                this.PanelJuego.add(temp); //lo va a agregar al panel del juego
            }
        }
    }
    
    

